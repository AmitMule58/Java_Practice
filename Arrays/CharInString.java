package Array;
// How many characters in given String
public class CharInString {
public static void main(String[] args) {
	String x="MyNameIsAmit";
	int count=0;
	for(int i=0; i<x.length();i++) {
		if(x.length()!=' ');{
			count++;
		}
	}
	System.out.println("Count of Character is: "+count);
}
}
