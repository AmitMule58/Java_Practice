package ArrayList;

public class Employees {

	int id;
	String name;
	String salary;
	public Employees(int id, String name, String salary) {
		super();
		this.id = id;
		this.name = name;
		this.salary = salary;
	}
	@Override
	public String toString() {
		return "Employees [id=" + id + ", name=" + name + ", salary=" + salary + "]";
	}
	
}
	
	
	
	
	

